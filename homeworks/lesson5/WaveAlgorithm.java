    public static String drawField(int[][] array) {
        String result = "";
        for (int[] line : array) {
            for (int item : line) {
                if (item == -1) {
                    result += "■■ ";
                } else if (item == 0) {
                    result += "□□ ";
                } else if (item > 0 && item < 10) {
                    result += "0" + item + " ";
                } else {
                    result += item + " ";
                }
            }
            result += "\n";
        }
        return result;
    }
